/**
 * ابحث عن تصنيف بناءً على الكلمات المفتاحية
 */
export function findCategoryByKeywords(text: string): SiteCategory | null {
  const lowerText = text.toLowerCase()

  for (const category of SITE_CATEGORIES) {
    for (const keyword of category.keywords) {
      if (lowerText.includes(keyword.toLowerCase())) {
        return category
      }
    }
  }

  return null
}

/**
 * احصل على تصنيف بناءً على ID
 */
export function getCategoryById(id: string): SiteCategory | null {
  return SITE_CATEGORIES.find(cat => cat.id === id) || null
}

/**
 * احصل على جميع أسماء التصنيفات بالعربية
 */
export function getAllCategoryNames(): string[] {
  return SITE_CATEGORIES.map(cat => cat.nameAr)
}

/**
 * احصل على أمثلة مشاريع من تصنيف معين
 */
export function getCategoryExamples(categoryId: string): string[] {
  const category = getCategoryById(categoryId)
  return category ? category.examples : []
}
