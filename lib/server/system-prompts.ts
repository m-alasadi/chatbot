/**
 * دالة للحصول على System Prompt مع إمكانية التخصيص (اختياري)
 */
export function getSiteSystemPrompt(
  customInstructions?: string
): string {
  let prompt = SITE_BOT_SYSTEM_PROMPT

  if (customInstructions) {
    prompt += `\n\n## تعليمات إضافية خاصة بهذه الجلسة:\n${customInstructions}`
  }

  return prompt
}

/**
 * دالة للحصول على رد fallback حسب الحالة
 */
export function getFallbackResponse(
  type: "no_results" | "api_error" | "api_quota_exceeded" | "out_of_scope"
): string {
  switch (type) {
    case "no_results":
      return FALLBACK_NO_RESULTS
    case "api_error":
      return FALLBACK_API_ERROR
    case "api_quota_exceeded":
      return FALLBACK_API_QUOTA_EXCEEDED
    case "out_of_scope":
      return FALLBACK_OUT_OF_SCOPE
    default:
      return FALLBACK_NO_RESULTS
  }
}
