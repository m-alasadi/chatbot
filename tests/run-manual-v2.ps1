$outputPath='tests/manual-7-questions-output-after-fix-v2.txt'
$questions=@(
'?? ?? ?????? ????? ?????? ????????',
'?? ?? ??????? ?????? ?????? ????????',
'?? ??? ?????? ???????? ?????',
'?? ??? ?????? ???????? ?????? ??????',
'??? ???? ?????? ???? ????? ???????',
'?? ??? ?????? ???????? ?????? ??????',
'?? ?????? ???????? ?????? ????? ?? ????'
)
$lines=New-Object System.Collections.Generic.List[string]
$summaries=New-Object System.Collections.Generic.List[string]
$lines.Add(("Timestamp: {0}" -f (Get-Date -Format 'yyyy-MM-dd HH:mm:ss')))
$lines.Add('Endpoint: http://localhost:3001/api/chat/site')
$lines.Add('============================================================')
for($i=0;$i -lt $questions.Count;$i++){
  $q=$questions[$i]; $idx=$i+1
  $lines.Add(("Question {0}: {1}" -f $idx,$q))
  $lines.Add('------------------------------------------------------------')
  try {
    $jsonBody=@{messages=@(@{role='user';content=$q});use_tools=$true} | ConvertTo-Json -Depth 6
    $resp=Invoke-RestMethod -Uri 'http://localhost:3001/api/chat/site' -Method Post -ContentType 'application/json; charset=utf-8' -Body $jsonBody -TimeoutSec 12 -ErrorAction Stop
    $respText=if($resp -is [string]){$resp}else{$resp | ConvertTo-Json -Depth 20}
    $lines.Add($respText)
    $first=(($respText -split "`r?`n") | Where-Object {$_ -match '\S'} | Select-Object -First 1)
    if(-not $first){$first='[Empty response]'}
    $summaries.Add(("{0}) {1}" -f $idx,$first))
  } catch {
    $err=$_ | Out-String
    $lines.Add('ERROR:')
    $lines.Add($err.TrimEnd())
    $firstErr=(($err -split "`r?`n") | Where-Object {$_ -match '\S'} | Select-Object -First 1)
    if(-not $firstErr){$firstErr='[Unknown error]'}
    $summaries.Add(("{0}) ERROR: {1}" -f $idx,$firstErr))
  }
  $lines.Add('============================================================')
}
$lines | Out-File -FilePath $outputPath -Encoding utf8
Write-Output "Saved output to $outputPath"
Write-Output 'Answer first-line summary:'
$summaries
